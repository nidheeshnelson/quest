//Arithmatic operations

var a=8;
var b=6;

//addition
document.write(a+b);
document.write("<br>");
//subtraction
document.write(a-b);
document.write("<br>");
//multiplication
document.write(a*b);
document.write("<br>");
//division
document.write(a/b);
document.write("<br>");
//modulus
document.write(a%b);
document.write("<br>");
//increment
a++;
document.write(a);
document.write("<br>");
//decrement
b--;
document.write(b);
document.write("<br>");
//exponential
document.write(b**2);
